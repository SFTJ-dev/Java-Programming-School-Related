public class IfElse{
    
    public static void main(String[] args){
        
        int age = 48;
        
        if(age>=18){ //true
            System.out.println("You're age is" + " " + age + ", " + "you can vote.");
        }else{ //false
            System.out.println("You're age is" + " " + age + ", " + "you can't vote.");
        }
        
    }
    
}